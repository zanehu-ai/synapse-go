// ── Request-ID ────────────────────────────────────────────────────────────────

func TestRequestIDRoundTrip(t *testing.T) {
	ctx := obs.WithRequestID(context.Background(), "abc-123")
	got, ok := obs.RequestIDFromContext(ctx)
	if !ok {
		t.Fatal("RequestIDFromContext: expected ok=true")
	}
	if got != "abc-123" {
		t.Errorf("got %q, want abc-123", got)
	}
}

